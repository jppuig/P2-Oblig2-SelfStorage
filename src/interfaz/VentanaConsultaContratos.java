    //Boton Mostrar datos del contrato seleccionado
    private void jButtonMostrarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMostrarDatosActionPerformed
        Contrato c = (Contrato) jListContratos.getSelectedValue();
        if(c != null){
            jTextAreaInfoContrato.setText("Id: "+ c.getIdContrato() + "\nCliente: " + c.getCliente() + "\nEmpleado: "+ c.getEmpleado() + "\nDeposito: " + c.getDeposito() + "\nDetalle: " + c.getDetalle());
            String[] info = new String[3];
            modeloTabla.setRowCount(0);
            for (Visita v : modelo.ordenarVisitasPorFecha(c)){       
                if (v.getContrato().equals(jListContratos.getSelectedValue())){
                    info[0] = valueOf(v.getDia());
                    info[1] = valueOf(v.getMes());
                    info[2] = v.getEmpleado().toString();
                    modeloTabla.addRow(info);
                }
            }
            jTableVisitasContrato.setModel(modeloTabla);
        }
        else {
            JOptionPane.showMessageDialog(this, "No hay contratos seleccionados", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButtonMostrarDatosActionPerformed

