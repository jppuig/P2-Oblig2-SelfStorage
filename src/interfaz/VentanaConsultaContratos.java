    //Boton dar contrato de baja
    private void jButtonBajaContratoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBajaContratoActionPerformed
        if(jListContratos.getSelectedIndex() != -1){
            Contrato c = (Contrato) jListContratos.getSelectedValue();
            
            ArchivoGrabacion arch = new ArchivoGrabacion(c.getCliente().toString() + " " + c.getIdContrato() + ".txt");
            for (Visita v : modelo.ordenarVisitasPorFecha(c)) {
                arch.grabarLinea(v.toString());
                modelo.eliminarVisita(v);
            }
            arch.cerrar();
            
            c.getDeposito().setContratado(false);
            modelo.eliminarContrato(c);
            JOptionPane.showMessageDialog(this, "Se dio de baja el contrato con éxito.", "Contrato dado de baja", JOptionPane.INFORMATION_MESSAGE);
            jListContratos.setListData(modelo.getListaContratos().toArray());
            jTextAreaInfoContrato.setText("");
            modeloTabla.setRowCount(0);
            jTableVisitasContrato.setModel(modeloTabla);
        }
        else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un contrato de la lista.", "Error", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_jButtonBajaContratoActionPerformed
